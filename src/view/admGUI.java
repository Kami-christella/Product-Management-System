     private boolean isDateInFuture(Date date) {
   // Date currentDate = new Date();
    
    // Create a Calendar instance for the current date
    Calendar currentCalendar = Calendar.getInstance();
    currentCalendar.setTime(currentDate);

    // Create a Calendar instance for the input date
    Calendar inputCalendar = Calendar.getInstance();
    inputCalendar.setTime(date);

    // Compare the input date with the current date
    return inputCalendar.after(currentCalendar);
}
