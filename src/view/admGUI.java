     public static boolean isDateAfter(Date date) {
        // Create a Calendar instance
        Calendar RDate = Calendar.getInstance();
        RDate.set(2023, Calendar.NOVEMBER, 24);

        // Create a Calendar instance for the provided date
        Calendar providedDate = Calendar.getInstance();
        providedDate.setTime(date);

        // Compare the provided date with November 24, 2023
        return providedDate.after(RDate);
    }
    
